async def run_browser_agent(task: str, save_dir: Path, headful: bool = False) -> bool:
    """
    Run a browser-use Agent with the given task.
    Downloads land in save_dir via Browser(downloads_path=...).
    Returns True if the agent reported done.
    """
    from browser_use import Agent, Browser
    from playwright_stealth import Stealth

    save_dir.mkdir(parents=True, exist_ok=True)
    llm = _make_llm()

    browser = Browser(
        headless=not headful,
        downloads_path=str(save_dir),
        disable_security=True,
        user_agent=_STEALTH_UA,
        ignore_default_args=["--enable-automation"],
        enable_default_extensions=True,
        minimum_wait_page_load_time=1.5,
        wait_between_actions=1.0,
        args=[
            '--no-sandbox',
            '--disable-setuid-sandbox',
            '--disable-dev-shm-usage',
            '--disable-gpu',
            '--disable-blink-features=AutomationControlled',
            '--window-size=1920,1080',
        ],
    )
    await browser.start()
    stealth = Stealth(
        chrome_runtime=True, navigator_webdriver=True, navigator_plugins=True, webgl_vendor=True,
        webgl_vendor_override="Google Inc. (Intel)",
        webgl_renderer_override="ANGLE (Intel, Intel(R) UHD Graphics 620 Direct3D11 vs_5_0 ps_5_0, D3D11)",
    )
    await browser._cdp_add_init_script(stealth.script_payload)
    logger.info("[master] Stealth fingerprint patches injected")

    agent = Agent(
        task=task,
        llm=llm,
        browser=browser,
        max_failures=3,
        use_judge=False,
    )

    logger.info("[Agent] Starting browser-use agent...")
    try:
        history = await agent.run()
        done = history.is_done()
        if not done:
            logger.warning("[Agent] Agent did not complete all steps within budget")
        return done
    except Exception as e:
        logger.error(f"[Agent] Run failed: {e}")
        logger.debug(traceback.format_exc())
        return False


