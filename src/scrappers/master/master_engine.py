def extract_zips(directory: Path) -> list[Path]:
    """
    Extract all zip files in directory and delete the zips.
    Returns list of extracted file paths.
    """
    import zipfile
    extracted = []
    for zip_path in sorted(directory.glob("*.zip"), key=lambda p: p.stat().st_size, reverse=True):
        try:
            logger.info(f"Extracting {zip_path.name}...")
            with zipfile.ZipFile(zip_path, 'r') as zf:
                for member in zf.namelist():
                    zf.extract(member, directory)
                    extracted.append(directory / member)
                    logger.info(f"  -> {member}")
            zip_path.unlink()
        except zipfile.BadZipFile:
            logger.warning(f"Skipping {zip_path.name} ({zip_path.stat().st_size} bytes) — not a valid zip, likely a duplicate/stub download. Deleting.")
            zip_path.unlink()
    return extracted


def find_table_csv(directory: Path, table_name: str) -> Optional[Path]:
    """
    Find a CSV file for table_name in directory — case-insensitive,
    allows extra suffixes like _csv or _export in the filename.
    """
    table_lower = table_name.lower()
    for f in directory.iterdir():
        if f.suffix.lower() == '.csv' and table_lower in f.stem.lower():
            return f
    return None


