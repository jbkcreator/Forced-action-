async def run_master_pipeline(
    county_id: str = "hillsborough",
    skip_download: bool = False,
    load_to_db: bool = False,
    headful: bool = False,
):
    cfg = get_county_config(county_id)
    source = cfg["sources"]["master_data"]
    bulk_tables = source.get("bulk_tables")  # Pinellas only

    ref_dir = Path(f"data/reference/{county_id}")
    staging_dir = Path(f"data/raw/master/{county_id}/new")

    logger.info("=" * 70)
    logger.info(f"MASTER PIPELINE — {cfg['display_name'].upper()}")
    logger.info(f"headful={headful}  load_to_db={load_to_db}")
    logger.info("=" * 70)

    # ── Phase 1: Download ─────────────────────────────────────────────────
    if not skip_download:
        task = build_agent_task(source, ref_dir)
        success = await run_browser_agent(task, ref_dir, headful=headful)
        if not success:
            logger.error("Agent reported failure — check browser output")
            # Continue anyway: partial downloads may still be usable

    # ── Discover + normalize downloaded files ────────────────────────────
    if bulk_tables:
        # Multi-table county: extract zips, find each table file individually.
        # Each table may arrive as plain CSV, zipped CSV, XLS, XLSX, or DBF.
        extract_zips(ref_dir)
        downloaded = []
        for table in bulk_tables:
            csv_path = find_table_csv(ref_dir, table)
            if csv_path:
                downloaded.append(csv_path)
                logger.info(f"Found {table} -> {csv_path.name}")
            else:
                logger.warning(f"Table not found after extraction: {table}")

        logger.info(f"Found {len(downloaded)}/{len(bulk_tables)} tables in {ref_dir}")
        if not downloaded:
            logger.error("No table files found — aborting")
            return

        if load_to_db:
            for csv_path in downloaded:
                load_to_database(csv_path, county_id)
        else:
            logger.info("Files ready. Run with --load-to-db to load into database.")
        return

    # Single-file county: any format, possibly zipped.
    # discover_and_convert handles CSV / XLSX / XLS / DBF, plain or inside a ZIP.
    converted_csv = discover_and_convert(ref_dir, staging_dir)
    if not converted_csv:
        logger.error(f"No bulk file found in {ref_dir} — download may have failed")
        return

    # ── Phase 3: Deduplicate ──────────────────────────────────────────────
    existing_ids = get_existing_parcel_ids(county_id)
    deduped_csv, new_count = deduplicate_csv(converted_csv, existing_ids, staging_dir)
    if not deduped_csv:
        logger.info("No new records — database is already up to date.")
        return

    # ── Phase 4: Load ─────────────────────────────────────────────────────
    if load_to_db:
        load_to_database(deduped_csv, county_id)
    else:
        logger.info(f"Ready: {deduped_csv} ({new_count:,} new records)")
        logger.info("Run with --load-to-db to insert into database.")


if __name__ == "__main__":
    parser = argparse.ArgumentParser(description="Master Property Data Pipeline")
    parser.add_argument("--county-id", default="hillsborough")
    parser.add_argument("--skip-download", action="store_true",
                        help="Skip download, use existing file in data/reference/<county>/")
    parser.add_argument("--load-to-db", action="store_true",
                        help="Load to database after download/conversion")
    parser.add_argument("--headful", action="store_true",
                        help="Run browser in visible (headful) mode")

    args = parser.parse_args()
    asyncio.run(run_master_pipeline(
        county_id=args.county_id,
        skip_download=args.skip_download,
        load_to_db=args.load_to_db,
        headful=args.headful,
    ))
