def _make_llm():
    from browser_use import ChatAnthropic
    from config.settings import get_settings
    settings = get_settings()
    return ChatAnthropic(
        model=BROWSER_MODEL,
        timeout=180,
        api_key=settings.anthropic_api_key.get_secret_value(),
        temperature=BROWSER_TEMPERATURE,
    )


def build_agent_task(source: dict, save_dir: Path) -> str:
    """
    Generate a browser-use agent task prompt from the county source config.

    Uses Claude to turn source metadata (url, description, navigation_hint,
    bulk_tables) into a clear step-by-step download task.  Falls back to a
    template if the LLM call fails.
    """
    import anthropic
    from config.settings import get_settings

    url = source["url"]
    description = source.get("description", "")
    nav_hint = source.get("navigation_hint", "") or ""
    bulk_tables = source.get("bulk_tables")

    meta = {
        "url": url,
        "description": description,
        "navigation_hint": nav_hint,
        "output_format": source.get("output_format", "csv"),
        "bulk_tables": bulk_tables,
        "save_directory": str(save_dir),
    }

    system_prompt = (
        "You generate browser-automation task instructions for a browser-use Agent. "
        "The agent is a Chromium browser controller that can click, scroll, and trigger downloads. "
        "Write a concise, step-by-step task in plain English. "
        "Be specific about file names to look for. "
        "Do NOT add any explanation outside the task text."
    )

    user_prompt = f"""Generate a browser-use agent task to download bulk property data files.

Source metadata:
{json.dumps(meta, indent=2)}

Requirements:
- The agent must navigate to the URL and download all relevant files.
- If bulk_tables is provided, download each one by name.
- If no bulk_tables, download the primary bulk property data file (largest or most prominent download link).
- Files will be saved automatically by the browser to the save_directory.
- Be explicit about what to click and what filenames to look for.
- Keep the task under 200 words.
"""

    try:
        settings = get_settings()
        client = anthropic.Anthropic(
            api_key=settings.anthropic_api_key.get_secret_value()
        )
        response = client.messages.create(
            model="claude-sonnet-4-6",
            max_tokens=512,
            temperature=0,
            system=system_prompt,
            messages=[{"role": "user", "content": user_prompt}],
        )
        task = response.content[0].text.strip()
        logger.info(f"[LLM] Generated agent task:\n{task}")
        return task
    except Exception as e:
        logger.warning(f"[LLM] Task generation failed ({e}) — using template fallback")
        return _template_task(source, save_dir)


def _template_task(source: dict, save_dir: Path) -> str:
    """Template fallback when LLM is unavailable."""
    url = source["url"]
    nav_hint = source.get("navigation_hint", "") or ""
    bulk_tables = source.get("bulk_tables")

    if bulk_tables:
        tables_str = "\n".join(f"- {t}" for t in bulk_tables)
        return (
            f"Go to {url}.\n"
            f"{nav_hint}\n"
            f"Download the following CSV files one by one:\n{tables_str}\n"
            f"Wait for each download to complete before starting the next."
        )
    return (
        f"Go to {url}.\n"
        f"{nav_hint}\n"
        f"Find and download the main bulk property data file on the page.\n"
        f"Wait for the download to complete."
    )


