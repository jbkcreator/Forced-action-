def _detect_file_type(file_path: Path) -> str:
    with open(file_path, 'rb') as fh:
        sig = fh.read(8)
    if not sig:
        raise ValueError(f"Empty file: {file_path}")
    if sig[0] in DBF_SIGNATURES:
        return 'dbf'
    if sig.startswith(b'\xD0\xCF\x11\xE0'):
        return 'xls'
    if sig.startswith(b'PK\x03\x04'):
        return 'xlsx'
    return 'text'


def _format_cell(value) -> str:
    if value is None:
        return ""
    if isinstance(value, Decimal):
        return format(value, 'f').rstrip('0').rstrip('.') or "0"
    if isinstance(value, float):
        import math as _math
        if _math.isnan(value):
            return ""
        if value.is_integer():
            return str(int(value))
    return str(value)


def _normalize_col(label: str) -> str:
    n = ''.join(c.lower() if c.isalnum() else '_' for c in str(label).strip())
    return '_'.join(filter(None, n.split('_')))


def _excel_chunks(path: Path):
    import xlrd
    book = xlrd.open_workbook(str(path), on_demand=True)
    sheet = book.sheet_by_index(0)
    header = [sheet.cell_value(0, c) for c in range(sheet.ncols)]
    buf = []
    for r in range(1, sheet.nrows):
        buf.append({header[c]: _format_cell(sheet.cell_value(r, c)) for c in range(sheet.ncols)})
        if len(buf) >= CHUNK_SIZE:
            df = pd.DataFrame(buf)
            df.columns = [_normalize_col(c) for c in df.columns]
            yield df
            buf = []
    if buf:
        df = pd.DataFrame(buf)
        df.columns = [_normalize_col(c) for c in df.columns]
        yield df
    book.release_resources()


def _dbf_chunks(path: Path):
    from dbfread import DBF
    tbl = DBF(str(path), encoding=RAW_FILE_ENCODING, ignore_missing_memofile=True)
    fields = tbl.field_names
    buf = []
    for rec in tbl:
        buf.append({f: _format_cell(rec.get(f)) for f in fields})
        if len(buf) >= CHUNK_SIZE:
            df = pd.DataFrame(buf)
            df.columns = [_normalize_col(c) for c in df.columns]
            yield df
            buf = []
    if buf:
        df = pd.DataFrame(buf)
        df.columns = [_normalize_col(c) for c in df.columns]
        yield df


def _xlsx_chunks(path: Path):
    """Read XLSX in chunks via pandas + openpyxl."""
    logger.info(f"Opening XLSX: {path}")
    for chunk in pd.read_excel(path, chunksize=CHUNK_SIZE, dtype=str, engine='openpyxl'):
        chunk.columns = [_normalize_col(c) for c in chunk.columns]
        yield chunk


def convert_to_csv(src: Path, dest_dir: Path) -> Path:
    """
    Convert XLS / XLSX / DBF to CSV. Plain CSV is copied as-is.
    Returns output CSV path.
    """
    dest_dir.mkdir(parents=True, exist_ok=True)
    ts = datetime.now().strftime("%Y%m%d_%H%M%S")
    out = dest_dir / f"master_{ts}.csv"
    ftype = _detect_file_type(src)
    logger.info(f"Converting {ftype.upper()} to CSV: {src.name}")

    if ftype == 'text':
        import shutil as _shutil
        _shutil.copy2(src, out)
        logger.info(f"Copied CSV: {out} ({out.stat().st_size / 1e6:.1f} MB)")
        return out

    if ftype == 'xls':
        reader = _excel_chunks(src)
    elif ftype == 'xlsx':
        reader = _xlsx_chunks(src)
    elif ftype == 'dbf':
        reader = _dbf_chunks(src)
    else:
        raise ValueError(f"Unsupported file type '{ftype}' for {src}")

    total = 0
    for i, chunk in enumerate(reader):
        for col in chunk.select_dtypes(include='object').columns:
            chunk[col] = chunk[col].str.strip()
        chunk.to_csv(out, mode='w' if i == 0 else 'a', index=False, header=(i == 0))
        total += len(chunk)

    logger.info(f"Converted {total:,} rows to {out} ({out.stat().st_size / 1e6:.1f} MB)")
    return out


def discover_and_convert(ref_dir: Path, staging_dir: Path) -> Optional[Path]:
    """
    Find the downloaded bulk file, extract zip if needed, convert to CSV.

    Handles every combination a county portal might deliver:
      - Plain CSV          -> use directly
      - CSV in ZIP         -> extract, use directly
      - XLS / XLSX / DBF   -> convert to CSV
      - XLS/XLSX/DBF in ZIP -> extract, then convert

    Priority: CSV > XLSX > XLS > DBF (prefer already-CSV to skip conversion).
    """
    extract_zips(ref_dir)  # no-op if no zips present

    for pattern in ['*.csv', '*.CSV', '*.xlsx', '*.XLSX', '*.xls', '*.XLS', '*.dbf', '*.DBF']:
        candidates = list(ref_dir.glob(pattern))
        if candidates:
            src = sorted(candidates, key=lambda p: p.stat().st_mtime, reverse=True)[0]
            logger.info(f"Found bulk file: {src.name} ({src.stat().st_size / 1e6:.1f} MB)")
            ftype = _detect_file_type(src)
            if ftype == 'text':
                return src  # already CSV, use in place
            return convert_to_csv(src, staging_dir)

    return None


